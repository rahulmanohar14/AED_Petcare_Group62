    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        int petid = 1;
        
        try {
            Object selectedItem = jComboBox1.getSelectedItem();
    
            petid = Integer.parseInt(selectedItem.toString());
    
        } catch (NumberFormatException e) {
            System.out.println("Error: The selected item is not a valid integer.");
        } catch (Exception e) {
            
        }

        //int petid = 1;
        dbHandler = new DatabaseHandler();
        Map<String, Object> petDetails = dbHandler.getpetinfo(petid);
        String name = (String) petDetails.get("name");
        String breed = (String) petDetails.get("breed");
        int age = (int) petDetails.get("age");
        int weight = (int) petDetails.get("weight");
        String gender = (String) petDetails.get("gender");
        String medical_history = (String) petDetails.get("medical_history");
        String Age = String.valueOf(age);
        String Weight = String.valueOf(weight);
        jTextField1.setText(name);
        jTextField2.setText(breed);
        jTextField3.setText(Age);
        jTextField4.setText(Weight);
        jComboBox2.setSelectedItem(gender);
        jTextField5.setText(medical_history);
    }//GEN-LAST:event_jComboBox1ActionPerformed


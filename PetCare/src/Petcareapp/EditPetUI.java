    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Object selectedItem = jComboBox1.getSelectedItem();
        int petid = Integer.parseInt(selectedItem.toString());
        dbHandler = new DatabaseHandler();
        boolean result = dbHandler.deletePet(petid);

        if (result) {
            JOptionPane.showMessageDialog(this, "Pet Info deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete petInfo.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        EditPetUI editpetui = new EditPetUI(1);
        editpetui.setVisible(false);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Object selectedItem = jComboBox1.getSelectedItem();
        int petid = Integer.parseInt(selectedItem.toString());
        String name = jTextField1.getText();
        String breed = jTextField2.getText();
        int age = Integer.parseInt(jTextField3.getText());
        int weight = Integer.parseInt(jTextField4.getText());
        String gender = (String) jComboBox2.getSelectedItem();
        String medicalHistory = jTextField5.getText();
        
        dbHandler = new DatabaseHandler();
        boolean result = dbHandler.updatePet(petid, name, breed, age, weight, gender, medicalHistory);

        if (result) {
            JOptionPane.showMessageDialog(this, "Pet Info updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update pet Info.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        EditPetUI editpetui = new EditPetUI(1);
        editpetui.setVisible(false);
        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed




    public PetOwnerUI(String username, int user_id) {
        userid = user_id;
        initComponents();
        

        jLabel1.setText("Welcome, "+ username);
        dbHandler = new DatabaseHandler();
        
        jTextArea1.setEditable(false);
        String petdata = dbHandler.petinfo(userid);
        jTextArea1.setText(petdata);
        
        jTextArea2.setEditable(false);
        String allappointments = dbHandler.getallappointments(userid);
        jTextArea2.setText(allappointments);
        
        jTextArea3.setEditable(false);
        String itemdata = dbHandler.getallitems(userid);
        jTextArea3.setText(itemdata);
        
        String Allappointmentid = dbHandler.allappointmentid(userid);
        
        String[] items = Allappointmentid.split(" ");
        jComboBox1.removeAllItems();

        // Add new items to jComboBox1
        for (String item : items) {
            jComboBox1.addItem(item);
        }
        
        String allitemid = dbHandler.allbuyitemid(userid);
        String[] buyitem = allitemid.split(" ");
        
        jComboBox2.removeAllItems();
        
        for(String item : buyitem) {
            jComboBox2.addItem(item);
        }
        
    }


    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        
        String read_id = (String) jComboBox1.getSelectedItem();
        int app_id = Integer.parseInt(read_id);
        dbHandler = new DatabaseHandler();
        boolean result = dbHandler.deleteappointment(app_id);

        if (result) {
            JOptionPane.showMessageDialog(this, "Appointment deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete Appointment.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        String allappointments = dbHandler.getallappointments(userid);
        jTextArea2.setText(allappointments);
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String item_id = (String) jComboBox2.getSelectedItem();
        int ItemId = Integer.parseInt(item_id);
        dbHandler = new DatabaseHandler();
        boolean result = dbHandler.deleteitems(ItemId);

        if (result) {
            JOptionPane.showMessageDialog(this, "Item deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete Item.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        String itemdata = dbHandler.getallitems(userid);
        jTextArea3.setText(itemdata);
        
    }//GEN-LAST:event_jButton5ActionPerformed

